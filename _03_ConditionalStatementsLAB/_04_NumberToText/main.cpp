#include <iostream>


int main() {

    int input;
    std::cin >> input;

    if (input == 0) {
        std::cout << "zero" << "\n";
    } else if (input == 1) {
        std::cout << "one" << "\n";
    } else if (input == 2) {
        std::cout << "two" << "\n";
    } else if (input == 3) {
        std::cout << "three" << "\n";
    } else if (input == 4) {
        std::cout << "four" << "\n";
    } else if (input == 5) {
        std::cout << "five" << "\n";
    } else if (input == 6) {
        std::cout << "six" << "\n";
    } else if (input == 7) {
        std::cout << "seven" << "\n";
    } else if (input == 8) {
        std::cout << "eight" << "\n";
    } else if (input == 9) {
        std::cout << "nine" << "\n";
    } else {
        std::cout << "number too big" << "\n";
    }


    return 0;
}